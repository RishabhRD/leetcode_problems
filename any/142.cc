struct ListNode {
  int val;
  ListNode *next;
  ListNode(int x) : val(x), next(NULL) {}
};

class Solution {
public:
  ListNode *detectCycle(ListNode *head) {
    if(head == nullptr) return head;
    auto slow = head;
    auto fast = head;
    bool has_cycle = false;
    while(fast and fast->next){
      slow = slow->next;
      fast = fast->next->next;
      if(slow == fast) {
        has_cycle = true;
        break;
      }
    }
    if(not has_cycle) return nullptr;
    slow = head;
    while(slow != fast){
      slow = slow->next;
      fast = fast->next;
    }
    return slow;
  }
};

int main(){
  vector<ListNode> vec = {0, 1, 2};
  for(int i = 0; i < size(vec); i++){
    vec[i].next = &vec[(i + 1) % size(vec)];
  }
  Solution s;
  s.detectCycle(&vec[0]);
}
