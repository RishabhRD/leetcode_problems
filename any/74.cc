bool searchMatrix(vector<vector<int>> &matrix, int target) {
  const auto m = matrix.size();
  const auto n = matrix[0].size();
  auto matrix_index = [&](size_t i) -> pair<size_t, size_t> {
    return {i / n, i % n};
  };
  auto matrix_val = [&](size_t i) {
    const auto [x, y] = matrix_index(i);
    return matrix[x][y];
  };
  int low = 0;
  int high = m * n - 1;
  while (low <= high) {
    const int mid = (low + high) / 2;
    if (matrix_val(mid) == target) {
      return true;
    } else if (matrix_val(mid) > target) {
      high = mid - 1;
    } else {
      low = mid + 1;
    }
  }
  return false;
}

int main(int argc, const char **argv) {
  vector<vector<int>> matrix = {
      {1, 3, 5, 7}, {10, 11, 16, 20}, {23, 30, 34, 60}};
  cout << searchMatrix(matrix, 3) << endl;
  return 0;
}
